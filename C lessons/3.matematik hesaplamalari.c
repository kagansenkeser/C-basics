#include<stdio.h>
int main()
{
	int x=15;
	int y=3;
	// \n isareti yazi yazarken bir alt satira gecmek icin kullanilir
printf("%d\n",x+y);
printf("%d\n",x-y);
printf("%d\n",x*y);
printf("%d\n",x/y);
printf("%d\n",x%y);
	
	return 0;
}


